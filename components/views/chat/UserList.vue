<template>
  <div class="user-list hover-scroll">
    <TypographyText size="sm" color="body" class="heading">
      {{
        $t('pages.chat.members', { count: allParticipantsAlphaSorted.length })
      }}
    </TypographyText>
    <div class="list">
      <button
        v-for="user in allParticipantsAlphaSorted"
        :key="user.did"
        class="user"
        @click="showQuickProfile($event, user)"
      >
        <UiUserState :user="user" :conversation-id="conversationId" />
        <TypographyText
          class="ellipsis"
          font="heading"
          weight="bold"
          color="dark"
        >
          {{ user.name }}
        </TypographyText>
      </button>
    </div>
  </div>
</template>

<script lang="ts">
import Vue, { computed, ComputedRef } from 'vue'
import { conversationHooks } from '~/components/compositions/conversations'
import { User } from '~/libraries/Iridium/users/types'

export default Vue.extend({
  setup() {
    // @ts-ignore
    const $nuxt = useNuxtApp()
    const conversationId: ComputedRef<string | undefined> = computed(() => {
      return $nuxt.$route.params.id
    })

    const { allParticipantsAlphaSorted } = conversationHooks(
      conversationId.value,
    )

    return { conversationId, allParticipantsAlphaSorted }
  },
  methods: {
    showQuickProfile(e: MouseEvent, user: User) {
      setTimeout(() => {
        this.$store.commit('ui/setQuickProfile', {
          user,
          position: { x: e.x, y: e.y },
        })
      }, 0)
    },
  },
})
</script>

